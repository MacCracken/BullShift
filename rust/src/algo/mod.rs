    #[test]
    fn test_record_trade_result() {
        let mut engine = AlgoEngine::new();
        let params = AlgoParameters {
            symbols: vec!["AAPL".to_string()],
            ..Default::default()
        };
        let id = engine.add_strategy("test", AlgoStrategyType::Breakout, params);

        engine.record_trade_result(&id, 500.0).unwrap();
        engine.record_trade_result(&id, -200.0).unwrap();
        engine.record_trade_result(&id, 300.0).unwrap();

        let perf = &engine.get_strategy(&id).unwrap().performance;
        assert_eq!(perf.total_trades, 3);
        assert_eq!(perf.winning_trades, 2);
        assert_eq!(perf.losing_trades, 1);
        assert!((perf.total_pnl - 600.0).abs() < 0.01);
        assert!((perf.win_rate - 2.0 / 3.0).abs() < 0.01);
    }

